<script type="text/javascript">
    var XEIcon = (function() {
        return {
            init: function() {
                this.cache();
                this.bindEvent();

                return this;
            },
            cache: function() {
                this.$searchInput = $('.sub-search-input');
                this.$searchTitle = $('.search-title');
                this.$searchIconWrap = $('.search-icon-wrap');
                this.$searchArea = $('.search-area');
                this.$iconArea = $('.icon-area');
                this.$xeGlyphiconsList = $('.xe-glyphicons-list');

            },
            bindEvent: function() {
                var _this = this;

                $(window).on('load', _this.preventReloading);

                this.$searchInput.on('keyup', function(e) {
                    var value = e.target.value;

                    if(value) {
                        var $showIcons = $('.xi-x[class*=' + value + ']').parents('li');

                        _this.$searchArea.removeClass('hidden').find('.search-title h1').text("'" + value + "' icons");
                        _this.$iconArea.addClass('hidden');
                        _this.$searchIconWrap.html($showIcons.clone());

                    } else {
                        _this.$searchArea.addClass('hidden');
                        _this.$iconArea.removeClass('hidden');

                    }
                });

                this.$xeGlyphiconsList.on('click', 'a', function(e) {
                    e.preventDefault();

                    var _$this = $(this);
                    var className = _$this.find('.xi-x').get(0).className.replace(' xi-x', '')
                    var icon = "&#x" + window.getComputedStyle($('.' + className)[0], ':before').getPropertyValue("content").replace(/'|"/g, '').charCodeAt(0).toString(16) + ";";

                    $target.html(icon);

                    self.close();
                });
            },
            preventReloading: function() {
                if(!self.$target) {
                    alert('팝업을 재실행 하세요.');
                    self.close();
                }
            },
        };
    })();

    $(function() {
        XEIcon.init();
    });
</script>